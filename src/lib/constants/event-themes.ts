/**
 * Get the theme for a given event type.
 */
export function getEventTheme(type: EventType): EventTheme {
  return EVENT_THEMES[type] || EVENT_THEMES.general
}

/**
 * Get all event types as options for select dropdowns.
 */
export function getEventTypeOptions() {
  return Object.values(EVENT_THEMES).map(theme => ({
    value: theme.type,
    label: `${theme.emoji} ${theme.label}`,
    emoji: theme.emoji,
    color: theme.primaryColor,
  }))
}
